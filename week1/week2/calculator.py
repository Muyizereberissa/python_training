num1 = float(input("enter a number: "))
num2 = float(input("enter a second number: "))
sign = input("enter sign: ")
if sign == "+":
    print(num1 + num2)
elif sign == "*":
    print(num1 * num2)
elif sign == "-":
    print(num1 - num2)
elif sign == "/":
    print(num1 / num2)
else:
    print(num1 % num2)



